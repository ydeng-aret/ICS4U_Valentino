def insertion_sort(list):
  count = 0
  for i in range(1, len(list)):
    current = list[i]
    while i > 0 and list[i - 1] > current:
      count = count + 1
      list[i] = list[i - 1]
      i = i - 1
      list[i] = current
  print("Insertion Sort Count: " + str(count))
  return list

