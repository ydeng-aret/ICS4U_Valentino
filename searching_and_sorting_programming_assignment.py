def count_starting_letters(list, letter):
    count = 0
    for word in list:
        if word[0] == letter:
            count = count + 1
    return count

def count_ending_letter(list, letter):
    count = 0
    for word in list:
        if word[len(word) - 1] == letter:
            count = count + 1
    return count

def find_word(list, word):
    index = 0
    for words in list:
        if words == word:
            index = index + 1
            return index
